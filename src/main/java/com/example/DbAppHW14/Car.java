package com.example.DbAppHW14;

public class Car {
    public String Name;
    public String Manufacture;
    public String V;
    public String Color;
    public String Year;
    public String Type;
    Car(){}
    Car(String Name,String Manufacture,String V, String Color,String Year,String Type){
        this.Name=Name;
        this.Manufacture=Manufacture;
        this.V=V;
        this.Color=Color;
        this.Type=Type;
        this.Year=Year;
    }
}
